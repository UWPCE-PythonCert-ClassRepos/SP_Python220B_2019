"""
This module provides a subtraction operator
"""


class Subtracter(object):
    """ A class for substracting two operands."""

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Return two numbers subtracted together
        operand_1 = first number
        operand_2 = second number
        """
        return operand_1 - operand_2
