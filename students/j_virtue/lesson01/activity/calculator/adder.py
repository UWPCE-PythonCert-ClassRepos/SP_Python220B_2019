"""
Adder - Provide logic to sum two operands
"""


class Adder(object):
    """ A class for adding two operands."""

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Return two numbers added together
        operand_1 = first number
        operand_2 = second number
        """
        return operand_1 + operand_2
